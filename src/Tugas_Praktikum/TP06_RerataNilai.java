package Tugas_Praktikum;

public class TP06_RerataNilai {
    //Membuat Method
    public int average (int bil1, int bil2) {
        return (bil1 + bil2) / 2;
    }

    public double average (double bil1, double bil2) {
        return (bil1 + bil2) / 2;
    }

    public int average (int bil1, int bil2, int bil3) {
        return (bil1 + bil2 + bil3) / 3;
    }
}
